<h4>Definición y tipos de cookies.</h4> <br>
<p ALIGN = justify>Una cookie son pequeños archivos de información que se descargan en el ordenador, Smartphone o Tablet del 
  usuario al acceder  a determinadas páginas web para almacenar y recuperar información sobre la navegación que se efectúa desde 
  dicho equipo. A  través de las cookies, las páginas web recuerdan información sobre la visita de los usuarios, lo que permite
  que se proporcione mejor y más segura experiencia de navegación en el mismo. Las cookies se asocian tanto a usuarios anónimos,
  es decir, aquellos  que visitan los Portales sin identificarse o registrarse, como aquellos que si lo hacen.</p>
<p ALIGN = justify>Por otro lado, las cookies no son un virus o cualquier otro tipo de programa malicioso que pueda dañar 
  dispositivos de los usuarios. Las cookies no pueden borrar ni leer información del computador o dispositivo de los usuarios.</p>
<p ALIGN = justify>Las cookies se crean o actualizan en el computador o dispositivo del usuario de manera automática, cuando
  éste accede a la página web de LA EMPRESA, COSMIPRINT S.A.S lo cual permite a la misma o a las terceras personas que esta
  contrate, realizar seguimiento a las cookies del usuario y por ende a la información que estas cookies contienen u obtienen
  del usuario. Es importante aclarar que las cookies solo son leídas por el sitio web que las creó.</p>
<p ALIGN = justify>LA EMPRESA  COSMIPRINT S.A.S podrá compartir información obtenida a través de las cookies con personas
  externas o terceros  (aliados, clientes, proveedores o empresas vinculadas a LA EMPRESA COSMIPRINT S.A.S), con el fin de 
  mejorar los servicios al  usuario. Así mismo, la información que se recibe a través de las cookies será utilizada por LA 
  EMPRESA  COSMIPRINT S.A.S y los  anteriores terceros, para los fines descritos en el presente documento, y cualquiera de sus 
  actualizaciones.</p>
<p ALIGN = justify>Existen diferentes tipos de cookies, las cuales pueden ser clasificadas en función a quien las ha creado, 
  cuanto tiempo duran  y, finalmente, que finalidad tienen.</p>
<ul ALIGN = justify>
    <li>Creación - Cookies propias o de terceros: son propias cuando las cookies se gestionan desde el terminal o dominio de 
      un mismo editor, mientras que son de terceros, cuando no las envía el propio editor, sino otra entidad.</li>
    <li>Tiempo - Cookies de sesión y persistentes: en cuanto a las cookies de sesión, los datos recolectados sólo se almacenarán
    mientras el usuario este navegando por la página web. En el caso de las persistentes, los datos continúan almacenados en el
    terminal y se puede acceder a ellos durante un período de tiempo determinado.</li>
    <li>Finalidad - Cookies técnicas/personalización/análisis/publicitarias: las cookies técnicas son aquellas que permiten 
    controlar el tráfico y la comunicación de datos; las cookies de personalización, las que dejan a los usuarios acceder según 
    algunas características propias que se recogen (navegador, idioma, etc.), como, por ejemplo, personalizar la página de 
    inicio del buscador. Las cookies de análisis recogen datos sobre el comportamiento de los usuarios y permiten elaborar 
    un perfil de usuario. De este modo se pueden analizar y detectar hábitos de navegación y mejorar la web, blog o e-Commerce 
    para adaptarse a las necesidades de navegación de los usuarios. Por último, las publicitarias recogen datos sobre la gestión
    de los espacios publicitarios. Este tipo de cookies permiten, por ejemplo, mostrarle a los usuarios banners de publicidad
    de la que puedan estar potencialmente interesados.</li>
</ul><br>

